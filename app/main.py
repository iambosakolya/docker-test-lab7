# Проста функція додавання двох чисел
def add(a, b):
    return a + b

# Функція множення двох чисел
def multiply(a, b):
    return a * b

# Якщо файл запущено напряму, виконується приклад використання
if __name__ == "__main__":
    print("Додавання: 3 + 5 =", add(3, 5))  # Очікувано: 8
    print("Множення: 4 * 6 =", multiply(4, 6))  # Очікувано: 24